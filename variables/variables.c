// Variables in C

#include <stdio.h>

int main() {
	int num1; // variable declaration
	num1 = 10; // variable definition
	int num2 = 20; // variable initilization
	printf("%d\n", num1); // prints 10
	printf("%d\n", num2); // prints 20
	num1 = 15; // variable re-assignment
	printf("%d\n", num1); // prints 15 
	
	return (0);
}
