#include <stdio.h>

int main() {
	// left shift operations
	printf("%d\n", 5 << 1);
	printf("%d\n", 6 << 1);
	printf("%d\n", 7 << 1);
	printf("%d\n", 8 << 1);
	printf("%d\n", 9 << 1);
	
	// right shift operations
	printf("%d\n", 5 >> 1);
	printf("%d\n", 6 >> 1);
	printf("%d\n", 7 >> 1);
	printf("%d\n", 8 >> 1);
	printf("%d\n", 9 >> 1);

}
